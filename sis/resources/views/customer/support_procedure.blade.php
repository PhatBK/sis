II. Quy trình giải quyết các thủ tục trên:

Giấy vay vốn: nhận và trả cho SV theo lớp (Lớp trưởng nhận):
- SV có nhu cầu vay vốn sẽ tự in biểu mẫu 6 (trang http://ctt-daotao.hust.edu.vn/).

- Nộp cho lớp trưởng.

- Lớp trưởng thu, xếp theo thứ tự a,b,c theo danh sách lớp, nộp cho CBQN kèm theo Danh sách SV vay vốn (Lớp trưởng ghi rõ số ĐT và địa chỉ email ở cuối danh sách)

Cấp giấy chứng nhận sinh viên tạm thời:
Chỉ xác nhận cho trường hợp mất, hỏng thẻ SV (đối với tất cả các khóa):

Sinh viên cần cấp chứng nhận SV phải:

- Nộp 1 ảnh 3x4 cm tại bàn 2 để được cấp mẫu

- Nộp mẫu cho CBQN kiểm tra, xác nhận

- Nộp mẫu đã xác nhận tại bàn 2 để ký, lấy dấu

Xác nhận làm vé tháng xe bus:
- SV nộp biểu mẫu cho CBQN theo lịch.

SV mượn hồ sơ cần:
- Nộp lại cho CBQN sổ hộ khẩu gốc.

- Đơn xin mượn hồ sơ.

Nhận quyết định thôi học, đình chỉ, xin tạm dừng:
SV gặp CBQN sáng thứ Hai, Ba, Tư làm thủ tục thanh toán ra trường và nộp thẻ SV

Thanh toán tốt nghiệp:
- SV năm cuối nhận Phiếu thanh toán ra trường từ lớp trưởng, xin xác nhận của các đơn vị (trừ xác nhận của CBQN) sau đó nộp cho lớp trưởng.

- Lớp trưởng thu, xếp theo thứ tự a,b,c theo danh sách lớp, nộp cho CBQN xác nhận và nhận lại Phiếu thanh toán ra trường để nộp về Viện.

* Chú ý: Sinh viên tốt nghiệp các đợt khác trong năm học cũng nhận Mẫu Phiếu thanh toán ra trường từ các Viện.

